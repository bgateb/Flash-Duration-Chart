// ---------------------------------------------------------------------------
// Parse labels into { manufacturer, model, mode }
// Labels follow these patterns:
//   "ELC 125 - Normal"        → model "ELC 125",         mode "Normal"
//   "ELC 125 - Action"        → model "ELC 125",         mode "Action"
//   "ELC Pro HD 1000"         → model "ELC Pro HD 1000", mode "Normal"
//   "ELB 400 HS A"            → model "ELB 400",         mode "HS A"
//   "ELB 500 TTL - Action"    → model "ELB 500 TTL",     mode "Action"
//   "Quadra HS B"             → model "Quadra",           mode "HS B"
// ---------------------------------------------------------------------------
function parseLabel(label) {
  const manufacturer = "Elinchrom";

  // Pattern 1: contains " - " separator
  if (label.includes(" - ")) {
    const [modelPart, mode] = label.split(" - ");
    return { manufacturer, model: modelPart.trim(), mode: mode.trim() };
  }

  // Pattern 2: ELB 400 with head type and port (e.g. "ELB 400 HS A")
  const elb400Match = label.match(/^(ELB 400)\s+(.+)$/);
  if (elb400Match) {
    return { manufacturer, model: elb400Match[1], mode: elb400Match[2] };
  }

  // Pattern 3: Quadra with head type and port (e.g. "Quadra HS A")
  const quadraMatch = label.match(/^(Quadra)\s+(.+)$/);
  if (quadraMatch) {
    return { manufacturer, model: quadraMatch[1], mode: quadraMatch[2] };
  }

  // Pattern 4: no mode suffix (e.g. "ELC Pro HD 1000")
  return { manufacturer, model: label, mode: "Normal" };
}

// Group SOURCE entries by flash model
const flashMap = new Map(); // model → { manufacturer, model, entries: [{mode, data}] }
for (const entry of SOURCE) {
  const { manufacturer, model, mode } = parseLabel(entry.label);
  if (!flashMap.has(model)) {
    flashMap.set(model, { manufacturer, model, entries: [] });
  }
  flashMap.get(model).entries.push({ mode, data: entry.data });
}

