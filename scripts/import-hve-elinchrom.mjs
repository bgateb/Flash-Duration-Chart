// Database import
// ---------------------------------------------------------------------------
const pool = createPool({
  host: env.MYSQL_HOST || "localhost",
  port: parseInt(env.MYSQL_PORT || "3306", 10),
  user: env.MYSQL_USER,
  password: env.MYSQL_PASSWORD,
  database: env.MYSQL_DATABASE,
  namedPlaceholders: true,
});

async function run() {
  const conn = await pool.getConnection();
  let flashesInserted = 0;
  let flashesSkipped = 0;
  let readingsInserted = 0;
  let readingsSkipped = 0;

  try {
    for (const { manufacturer, model, entries } of flashMap.values()) {
      const slug = slugify(`${manufacturer} ${model}`);

      // Upsert flash record
      const [existing] = await conn.execute(
        "SELECT id FROM flashes WHERE slug = ? LIMIT 1",
        [slug]
      );

      let flashId;
      if (existing.length > 0) {
        flashId = existing[0].id;
        console.log(`SKIP flash (already exists): ${manufacturer} ${model} (id=${flashId})`);
        flashesSkipped++;
      } else {
        const [result] = await conn.execute(
          `INSERT INTO flashes (manufacturer, model, slug, notes)
           VALUES (?, ?, ?, ?)`,
          [manufacturer, model, slug, ATTRIBUTION]
        );
        flashId = result.insertId;
        console.log(`INSERT flash: ${manufacturer} ${model} (id=${flashId}, slug=${slug})`);
        flashesInserted++;
      }

      // Insert readings
      for (const { mode, data } of entries) {
        for (const [powerStops, durationReciprocal] of data) {
          const stopsBelowFull = (-powerStops).toFixed(2);
          const tOnetenth = (1 / durationReciprocal).toFixed(7);

          try {
            await conn.execute(
              `INSERT INTO readings (flash_id, mode, stops_below_full, t_one_tenth_seconds)
               VALUES (?, ?, ?, ?)`,
              [flashId, mode, stopsBelowFull, tOnetenth]
            );
            readingsInserted++;
          } catch (err) {
            if (err.code === "ER_DUP_ENTRY") {
              readingsSkipped++;
            } else {
              throw err;
            }
          }
        }
      }
    }
  } finally {
    conn.release();
    await pool.end();
  }

  console.log("\n=== Done ===");
  console.log(`Flashes:  ${flashesInserted} inserted, ${flashesSkipped} skipped`);
  console.log(`Readings: ${readingsInserted} inserted, ${readingsSkipped} skipped`);
}

run().catch((err) => {
  console.error(err);
  process.exit(1);
});
